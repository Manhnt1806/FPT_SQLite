SELECT HONV, TENNV FROM NHANVIEN

--LOẠI BỎ GIÁ TRỊ TRÙNG LẶP VỚI DISTINCT	
--TRẢ VỀ CÁC BẢN GHI CÓ GIÁ TRỊ KHÔNG TRÙNG LẶP ÁP DỤNG VỚI CỘT DỮ LIỆU ĐẦU TIÊN
SELECT DISTINCT TENNV FROM NHANVIEN

--TRUY VẤN KẾT HỢP MỆNH ĐỀ WHERE
--LẤY RA TẤT CẢ NHÂN VIÊN CÓ HỌ TRẦN
SELECT * FROM NHANVIEN WHERE HONV = N'Trần'

SELECT * FROM NHANVIEN WHERE MANV IN ('001', '002', '005')

SELECT * FROM NHANVIEN WHERE TENNV = N'Tùng' AND HONV = N'Nguyễn'

SELECT * FROM NHANVIEN WHERE TENNV = N'Tùng' OR HONV = N'Nguyễn'

SELECT * FROM NHANVIEN WHERE MANV IN ('001', '002', '005') AND LUONG>=40000

SELECT * FROM NHANVIEN WHERE MANV IN ('001', '002', '005') AND LUONG>=40000 OR PHAI = N'Nam'

SELECT * FROM NHANVIEN WHERE MANV IN ('001', '002', '005') OR PHAI = N'Nam' AND LUONG>=40000 

--ĐIỀU KIỆN WHERE BETWEEN
SELECT * FROM NHANVIEN WHERE LUONG BETWEEN 20000 AND 35000
SELECT * FROM NHANVIEN WHERE NGSINH BETWEEN '1960-01-01' AND '1965-12-30'

--ĐIỀU KIỆN WHERE LIKE
--LẤY RA TẤT CẢ CỘT CÓ TENNV GIỐNG KÝ TỰ 'TÙNG'
SELECT * FROM NHANVIEN WHERE TENNV LIKE N'Tùng'
--LẤY RA TẤT CẢ CÁC CỘT CỦA BẢNG NHANVIEN CÓ TÊN NV BẮT ĐẦU BẰNG KÝ TỰ 'T'
SELECT * FROM NHANVIEN WHERE TENNV LIKE 'T%'
SELECT * FROM NHANVIEN WHERE DCHI LIKE '119%'
--LẤY RA TẤT CẢ CÁC CỘT CỦA BẢNG NHANVIEN CÓ TÊN NV KẾT THÚC BẰNG KÝ TỰ 'N'
SELECT * FROM NHANVIEN WHERE TENNV LIKE '%N'
SELECT * FROM NHANVIEN WHERE DCHI LIKE '%TP HCM'
--LẤY RA TẤT CẢ CÁC CỘT CỦA BẢNG NHANVIEN ĐỊA CHỈ CÓ KÝ TỰ 'CỐNG QUỲNH'
SELECT * FROM NHANVIEN WHERE DCHI LIKE N'%Cống Quỳnh%'
