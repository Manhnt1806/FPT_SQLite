--ORDER BY: SẮP XẾP
SELECT * FROM NHANVIEN ORDER BY LUONG ASC

SELECT * FROM NHANVIEN ORDER BY LUONG DESC
--GROUP BY: NHÓM CÁC DỮ LIỆU CÓ GIÁ TRỊ GIỐNG NHAU THÀNH MỘT NHÓM
SELECT PHG, MAX(LUONG) AS LUONG_MAX FROM NHANVIEN GROUP BY PHG
--HÀM TỔNG HỢP: TÊN HÀM (TÊN CỘT)
--MAX(CỘT): TÌM RA GTLN
--MIN(CỘT): TÌM RA GTNN
--AVG(CỘT): TÌM RA GIÁ TRỊ TB
--COUNT(CỘT):ĐẾM SỐ LƯỢNG
SELECT PHG, MIN(LUONG) LUONG_MIN FROM NHANVIEN GROUP BY PHG HAVING MIN(LUONG) > 25000
SELECT MAX(NGSINH) AS NGSINH_MAX FROM NHANVIEN
SELECT COUNT(*) AS SOLUONG FROM NHANVIEN
SELECT COUNT(MANV) AS SOLUONGNV FROM NHANVIEN
--ĐẶT LẠI TÊN CÁC CỘT QUA KEYWORDS
SELECT HONV AS HONHANVIEN, TENLOT AS TENLOTNHANVIEN, TENNV AS TENNHANVIEN FROM NHANVIEN
--THỰC HIÊN PHÉP TÍNH DỰA VÀO CÁC CỘT 
SELECT TENNV, (LUONG * 12) AS TONGTHUNHAP FROM NHANVIEN 
SELECT HONV, TENNV, (YEAR(GETDATE())-YEAR(NGSINH)) AS TUOI FROM NHANVIEN
SELECT TENNV, ((LUONG + 1000) * 12) AS TONGTHUNHAP FROM NHANVIEN WHERE PHAI = N'NỮ'

